package com.hongxu.ripple.gateway.style.skin;

public class PageSkin {
	
	private String styleType = null;
	private BackgroundColor bgColor = null;
	private ModuleSkin headerSkin = null;
	private ModuleSkin buttonSkin = null;
	private ModuleSkin navbarSkin = null;
	private ModuleSkin sidebarSkin = null;
	private ModuleSkin portletSkin = null;
	private ModuleSkin footerSkin = null;
	private BannerSkin bannerSkin = null;
	private PanelSkin  panelSkin = null;
	
	public String getStyleType() {
		return styleType;
	}
	
	public void setStyleType(String styleType) {
		this.styleType = styleType;
	}
	
	public BackgroundColor getBgColor() {
		return bgColor;
	}

	public void setBgColor(BackgroundColor bgColor) {
		this.bgColor = bgColor;
	}

	public ModuleSkin getHeaderSkin() {
		return headerSkin;
	}

	public void setHeaderSkin(ModuleSkin headerSkin) {
		this.headerSkin = headerSkin;
	}

	public ModuleSkin getButtonSkin() {
		return buttonSkin;
	}

	public void setButtonSkin(ModuleSkin buttonSkin) {
		this.buttonSkin = buttonSkin;
	}

	public ModuleSkin getNavbarSkin() {
		return navbarSkin;
	}


	public void setNavbarSkin(ModuleSkin navbarSkin) {
		this.navbarSkin = navbarSkin;
	}

	public BannerSkin getBannerSkin() {
		return bannerSkin;
	}

	public void setBannerSkin(BannerSkin bannerSkin) {
		this.bannerSkin = bannerSkin;
	}

	public ModuleSkin getSidebarSkin() {
		return sidebarSkin;
	}


	public void setSidebarSkin(ModuleSkin sidebarSkin) {
		this.sidebarSkin = sidebarSkin;
	}


	public PanelSkin getPanelSkin() {
		return panelSkin;
	}


	public void setPanelSkin(PanelSkin panelSkin) {
		this.panelSkin = panelSkin;
	}


	public ModuleSkin getPortletSkin() {
		return portletSkin;
	}


	public void setPortletSkin(ModuleSkin portletSkin) {
		this.portletSkin = portletSkin;
	}

	public ModuleSkin getFooterSkin() {
		return footerSkin;
	}

	public void setFooterSkin(ModuleSkin footerSkin) {
		this.footerSkin = footerSkin;
	}
	
	
}
