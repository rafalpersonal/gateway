package com.hongxu.ripple.gateway.style.metadata;

public class Item {
	
	private String name = null;
	private String url = null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}	
}
